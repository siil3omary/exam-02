#include <unistd.h>
int main(int  ac, char **av)
{
    if(ac == 2)
    {
        int j , c;
        int i;

        i = 0;
        while (av[1][i])
        {
        if(((av[1][i] >= 'A' && av[1][i] <= 'Z') || (av[1][i] >= 'a' && av[1][i] <= 'z')))
        {
            if(av[1][i] >= 'a' && av[1][i] <= 'z')
            {
              c = av[1][i] - 97;
                j = 0;
            }else if(av[1][i] >= 'A' && av[1][i] <= 'Z')
            {
                c = av[1][i] - 65;
                j = 0;                
            }



            while (j <= c )
            {
                write(1, &av[1][i], 1);
                j++;
            }
            
        }else 
            write(1, &av[1][i ], 1);
            i++;
            
        }

}
    write(1, "\n", 1);
}